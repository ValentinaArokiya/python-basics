# SORT and REVERSE
# sort() - sorts in-place
# sorted - sorted list is assigned to a new variable

new_list = ['a', 'e', 'c', 'x', 'z']
num_list = [4, 3, 8, 1]

new_list.sort() # sorts in-place.
print(new_list)

sorted_list = sorted(num_list)
print(sorted_list)


a = [5, 8, 2, 4]
a.reverse()
print("Reverse = {}" .format(a))



